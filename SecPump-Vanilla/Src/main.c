int _read(int fd, char *ptr, int len) {
  HAL_StatusTypeDef status;

  if (fd == STDIN_FILENO) {
    // if equal zero we got the mutex
    // arbitrary time-out
    status = HAL_UART_Receive(&huart2, (uint8_t *)ptr, 1, HAL_MAX_DELAY);

    if (status == HAL_OK) {
      return 1;
    } else {
      return EIO;
    }
  }
  return -1;
}

